# ==================== 工具入口 ====================

@tool
def quick_command(command: str) -> str:
    """处理用户的快捷指令请求。
    
    支持的指令：
    - /开黑：游戏陪伴模式，用游戏梗轻松聊天
    - /歌单：音乐推荐，根据情绪推荐对应音乐
    - /骂人：情绪宣泄模式，陪用户吐槽
    - /故事：治愈小故事
    - /锚点：今日小任务
    - /撑不住：紧急求助，触发L3/L4协议

    Args:
        command: 快捷指令名称（如：开黑、歌单、骂人、故事、锚点、撑不住）

    Returns:
        对应指令的引导回复
    """
    cmd = command.strip().lower().replace("/", "")

    if cmd in ["开黑", "游戏", "打游戏"]:
        return _game_mode()
    elif cmd in ["歌单", "音乐", "听歌"]:
        return _music_mode()
    elif cmd in ["骂人", "吐槽", "发泄"]:
        return _vent_mode()
    elif cmd in ["故事", "小故事", "听故事"]:
        return _story_mode()
    elif cmd in ["锚点", "任务", "小目标"]:
        return _anchor_mode()
    elif cmd in ["撑不住", "救命", "help", "紧急"]:
        return _emergency_mode()
    else:
        return (
            f"不太认识「{command}」这个指令呢~ 试试这些：\n\n"
            "🎮 /开黑 —— 来打游戏呀\n"
            "🎵 /歌单 —— 帮你挑首歌\n"
            "💢 /骂人 —— 陪你吐槽\n"
            "📖 /故事 —— 讲个小故事\n"
            "🎯 /锚点 —— 今天的小任务\n"
            "🆘 /撑不住 —— 需要紧急帮助"
        )